public partial class IonAccordion : IonComponent
{
    public IonAccordion() : base("ion-accordion") { }
    /// <summary>
    /// If `true`, the accordion cannot be interacted with.
    /// </summary>
    public bool disabled
    {
        get
        {
            return this.GetTag().GetAttribute<bool>("disabled");
        }
        set
        {
            if (!value) return;
            this.GetTag().SetAttribute("disabled", value.ToString());
        }
    }

    /// <summary>
    /// The mode determines which platform styles to use.
    /// </summary>
    public string mode
    {
        get
        {
            return this.GetTag().GetAttribute<string>("mode");
        }
        set
        {
            this.GetTag().SetAttribute("mode", value.ToString());
        }
    }

    /// <summary>
    /// If `true`, the accordion cannot be interacted with, but does not alter the opacity.
    /// </summary>
    public bool @readonly
    {
        get
        {
            return this.GetTag().GetAttribute<bool>("readonly");
        }
        set
        {
            if (!value) return;
            this.GetTag().SetAttribute("readonly", value.ToString());
        }
    }

    /// <summary>
    /// The toggle icon to use. This icon will be rotated when the accordion is expanded or collapsed.
    /// </summary>
    public string toggleIcon
    {
        get
        {
            return this.GetTag().GetAttribute<string>("toggleIcon");
        }
        set
        {
            this.GetTag().SetAttribute("toggleIcon", value.ToString());
        }
    }

    /// <summary>
    /// The slot inside of `ion-item` to place the toggle icon. Defaults to `"end"`.
    /// </summary>
    public string toggleIconSlot
    {
        get
        {
            return this.GetTag().GetAttribute<string>("toggleIconSlot");
        }
        set
        {
            this.GetTag().SetAttribute("toggleIconSlot", value.ToString());
        }
    }

    /// <summary>
    /// The value of the accordion. Defaults to an autogenerated value.
    /// </summary>
    public string value
    {
        get
        {
            return this.GetTag().GetAttribute<string>("value");
        }
        set
        {
            this.GetTag().SetAttribute("value", value.ToString());
        }
    }

    public static class Slot
    {
        /// <summary> 
        /// Content is placed below the header and is shown or hidden based on expanded state.
        /// </summary>
        public const string Content = "content";
        /// <summary> 
        /// Content is placed at the top and is used to expand or collapse the accordion item.
        /// </summary>
        public const string Header = "header";
    }
}

